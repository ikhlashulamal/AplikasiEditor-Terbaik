                <h2>Jenis Pupuk Organik dan Kimia, Beserta Cara Menyimpan yang Tepat</h2>
                <p>Pupuk menjadi unsur hara penting bagi tanaman. Meski sejak lama ada penggunaan pupuk organik, tak ayal banyak yang menggunakan pupuk kimia sebagai penyubur. Pupuk kimia memiliki kelebihan pada unsur dan senyawa yang mudah larut, serta cepat diserap oleh tanaman tanpa memerlukan proses penguraian.</p> <br>
                <p>Meski begitu, terkadang pupuk kimia mengandung kalsium amonium nitrat. Sebuah aplikasi senyawa paling umum dan dapat merangsang pertumbuhan tanaman untuk kebun yang bisa berbahaya di sisi lain.</p> <br>
                <h4>Pupuk Kimia</h4><br>
                <h6>- Urea</h6>
                <p>Pupuk urea menjadi yang paling diminati oleh petani, karena sangat bermanfaat untuk lahan pertanian maupun budidaya. Pupuk ini memiliki rumus kimia CO(NH2)2, terbuat dari campuran gas amoniak (NH3) dan gas asam arang.
                    Sekitar 46kg nitrogen terkandung dalam 100kg pupuk urea. Kandungan yang cukup tinggi tersebut mampu mempercepat pertumbuhan dan perkembangan tanaman. Sebab nitrogen akan memudahkan proses fotosintesis, sehingga menghasilkan lebih banyak klorofi.</p>            
                <br>
                <h6>- ZA (Zwavelzure Amonium)</h6>
                <p>Pupuk Zwavelzure Amonium mempunyai rumus kimia (NH4)2SO4 yang mengandung sekitar 21% nitrogen dan 24% sulfur. Biasanya diterapkan sebagai pupuk dasar oleh petani, sebab reaksi kerja yang agak lambat.
                Manfaat lain dari pupuk ZA, mampu menambah unsur hara pada tanaman. Kemudian memperbaiki kualitas tanaman, serta menambah nilai gizi pada hasil panen. Kelebihan lainnya, ZA juga bisa membantu tanaman agar terhindar dari hama.</p>
                <br>
                <h6>- SP-36 (super phosphate)</h6>
                <p>Pupuk SP-36 (super phosphate) atau tertulis P2O5 dalam rumus kimia. Pupuk ini dibuat dengan pencampuran asam sulfat (belerang) dengan fosfat alam. Memiliki peran utama sebagai penambah unsur hara phosphor pada tanaman. Biasanya digunakan di berbagai macam tanaman, seperti perkebunan dan holtikultura.
                    Pupuk SP-36 kerap digunakan petani untuk membantu tanaman menghasilkan buah yang lebih banyak. Kelebihan lain SP36, bisa membantu memperbaiki kualitas biji, merangsang pembelahan tanaman, mempercepat pemasakan buah, menguatkan batang tanaman, dan memperbesar jaringan sel.</p>    
                <br>
                <h6>- KCl (Kalium Klorida)</h6>
                <p>Pupuk KCl dibuat dari ekstraksi mineral kalim dan mengandung sekitar 60 persen kalium dalam bentuk K2O. Bentuknya bubuk atau serbuk merah.
                    Jenis pupuk yang mudah larut dalam air, sehingga mudah diserap oleh tanaman. Unsur klorida yang terkandung bersifat toksik atau racun bagi tanaman tertentu, seperti wortel dan kentang. Reaksi kimianya netral hingga masam. Cocok digunakan sebagai pupuk dasar atau pupuk susulan.</p>
                <br>
                <h6>- NPK Phonska (Nitrogen Phospate Kalium)</h6>
                <p>Pupuk NPK digunakan sebagai penyeimbang unsur hara makro dan mikro pada tanah. Sebab mengandung unsur zat hara yang paling banyak dan sangat dibutuhkan tanaman, yakni nitrogen, fosfat, kalium, magnesium, dan kalsium.
                    Kelebihan pupuk NPK, mencegah tanaman supaya tidak kerdil. Serta pertumbuhan akar jadi lebih kuat, banyak, dan panjang, sehingga mudah menyerap zat hara di tanah. Pupuk ini bisa diaplikasikan di berbagai jenis tanah, sebab menimbulkan reaksi kimia yang netral. Pupuk jenis ini bisa digunakan sebagai pupuk dasar atau pupuk susulan.</p>
                <br>
                <h6>- Dolomite (Kapur Karbonat)</h6>
                <p>Dolomite atau biasa dikenal dengan kapur pertanian memiliki manfaat sebagai penyedia unsur hara makro sekunder Ca dan Mg. Reaksi kimia yag ditimbulkan basa (alkali) sehingga menaikkan pH tanah.
                    Pupuk ini berbentuk butiran halus berwarna putih keabu-abuan atau putih kebiruan. Sifatnya mudah menyerap air dan mudah dihancurkan. Semakin halus butirannya, maka semakin baik kualitasnya.</p>
                <br>
                <h6>- Eco Farming</h6>
                <p>Eco Farming adalah pupuk organik dengan menggunakan untuk meningkatkan produksi para petani. Kehadiran eco farming juga bisa membantu para petani mengurangi penggunaan pupuk anorganik.</p>
